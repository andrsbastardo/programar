char	*ft_strchr(const char *s, int c)
{
    int i;
    char *str;

    *str = *s;
    i = 0;

    while (str[i] != '\0')
    {
        if (str[i] == c)
        {
            return((char *)s +i);
        }
        i++;
    }
    return(0);
    
}